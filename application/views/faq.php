<h1>Your questions, answered.</h1>
<p class="intro">Have questions about our hourly maintenance rate? We've done our best to provide some answers to the most common questions below.</p>

<h2>Why do maintenance with an hourly rate?</h2>
<p>Garden maintenance times vary from week to week and even more so from the rainy to dry season. For instance, the rate of growth of foliage during the dry season is substantially lower compared to that of the rainy season. Therefore, we often spend less time maintaining gardens during these months. Hourly billing provides a way to pass these savings onto you. Each time our crew completes a maintenance service on your garden, you will be asked to sign them out with the time. Your monthly bill is the cumulative time we've spent in your garden plus a fixed transportation fee.</p>

<h2>What size is the Maintenance Crew?</h2>
<p>The size of the maintenance crew varies. You will be informed and your rate adjusted if we ever remove crew members from your maintenance team.</p>

<h2>How can I be sure that the maintenance crew will spend excessive time in my garden making my bill higher?</h2>
<p>The maintenance manager is given incentives to finish your garden as quickly as possible while maintaining the highest standards. The maintenance crew will not benefit at all from spending more time in your garden, in fact this would actually be to their disadvantage. We also allow you to rate their service ensuring that you are satisfied 100% of the time.</p>

<h2>If there is heavy rain on my maintenance day, do I still pay for the service?</h2>
<p>You only pay for the time our crew spends in your garden doing maintenance work. You will not be charged if we miss a maintenance day because of bad weather or any other unforeseen events.</p>

<h2>How experienced is the maintenance crew?</h2>
<p>Our garden maintenance crew has a combined experience of over 10 years in landscape. What that means to you is more accurate diagnosis of common plant diseases, plant identification and better recommendations on how to grow your garden.</p>
 
